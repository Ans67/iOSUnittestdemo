import Foundation

class MathStuff {
    
    func additonStuff(x: Int , y: Int) -> Int{
        return x + y
    }
    
    func multiplication(x:Int , y:Int) -> Int{
        return x * y
    }
    
    func divide(x:Int , y:Int) -> Int{
        return x / y
    }
    
}
